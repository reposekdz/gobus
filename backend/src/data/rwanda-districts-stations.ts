export const getAllBusStations = () => {
  const allStations: Array<{province: string, district: string, station: string}> = [];
  
  Object.entries(rwandaDistrictsStations).forEach(([province, districts]) => {
    Object.entries(districts).forEach(([district, stations]) => {
      stations.forEach(station => {
        allStations.push({ province, district, station });
      });
    });
  });
  
  return allStations;
};

export const getStationsByDistrict = (district: string) => {
  for (const [province, districts] of Object.entries(rwandaDistrictsStations)) {
    if (districts[district]) {
      return districts[district];
    }
  }
  return [];
};

export const searchStations = (query: string) => {
  const allStations = getAllBusStations();
  return allStations.filter(station => 
    station.station.toLowerCase().includes(query.toLowerCase()) ||
    station.district.toLowerCase().includes(query.toLowerCase()) ||
    station.province.toLowerCase().includes(query.toLowerCase())
  );
};